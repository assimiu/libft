#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n >= 0 && n <= 9)
	{
		c = n + '0';
		write(fd, &c, 1);
	}
	else
	{
		if (n < 0)
		{
			if (n == -2147483648)
			{
				write(fd, "-2147483648", 11);
				return;
			}
			else
				n = (-1) * n;
		}
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
